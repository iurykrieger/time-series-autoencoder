START = datetime(2019, 4, 29, 0, 0) # 29/04/2019
END = datetime(2019, 7, 29, 0, 0) # 29/07/2019
STEP = 1000
RESOLUTION = '5m'

for api_key in APIKEYS:
    try:
        apikey_data_file = "metrics/raw/{api_key}_{start}_{end}.csv".format(
            api_key=api_key,
            start=START.strftime("%Y-%m-%d"),
            end=END.strftime("%Y-%m-%d")
        )
        print("Processing \"{api_key}\"...".format(api_key=apikey_data_file))

        if not path.isfile(apikey_data_file):
            features, values = get_all_views(api_key, START, END, STEP, RESOLUTION)
            write_metrics_file(apikey_data_file, features, values)

    except Exception as e:
        traceback.print_exc()
